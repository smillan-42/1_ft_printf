#include "libprintf.h"

int	ft_putnbr(int n, int i)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		i = i + ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		i = 1 + ft_putnbr(n / 10, i);
		ft_putnbr(n % 10, i);
	}
	else
	{
		i = i + ft_putchar(n + '0');
	}
	return (i);
}